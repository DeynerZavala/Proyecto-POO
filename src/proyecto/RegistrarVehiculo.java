    private void OpVehiculoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_OpVehiculoItemStateChanged
        String OpA = (String) OpVehiculo.getSelectedItem();
        if(OpA=="Transporte"){
            JTransporte t= new JTransporte();
            t.setSize(580,170);
            t.setLocation(0,0);
            ContenidoT.removeAll();
            ContenidoT.add(t,AbsoluteLayout.class);
            ContenidoT.revalidate();
            ContenidoT.repaint();
            TipoDeVehiculo2.setText("Vehiculo de Tranporte :");
            OpTransporte.setVisible(true);
            OpCarga.setVisible(false);
            ContenidoToC.removeAll();
            ContenidoToC.revalidate();
            ContenidoToC.repaint();
            
        }
        else if(OpA=="Carga"){
            JCarga c= new JCarga();
            c.setSize(580,170);
            c.setLocation(0,0);
            ContenidoT.removeAll();
            ContenidoT.add(c,AbsoluteLayout.class);
            ContenidoT.revalidate();
            ContenidoT.repaint();
            TipoDeVehiculo2.setText("Vehiculo de Carga :");
            OpCarga.setVisible(true);
            OpTransporte.setVisible(false);
            ContenidoToC.removeAll();
            ContenidoToC.revalidate();
            ContenidoToC.repaint();
            
        }
        else{
            ContenidoT.removeAll();
            ContenidoT.revalidate();
            ContenidoT.repaint();
            OpCarga.setVisible(false);
            OpTransporte.setVisible(false);
            TipoDeVehiculo2.setText("");
            ContenidoToC.removeAll();
            ContenidoToC.revalidate();
            ContenidoToC.repaint();
        }
    }//GEN-LAST:event_OpVehiculoItemStateChanged

