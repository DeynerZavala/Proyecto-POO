    private byte[] AbrirArchivo(File archivo){
        byte[] imagen= new byte[1024*1000];
        try{
            entrada=new FileInputStream(archivo);
            entrada.read(imagen);
        } catch(Exception e){
            System.out.println("ERROR");
        }
        return imagen;
    }
    private String GuardarArchivo(String direccion, byte[]imagen){
        String mensaje=null;
        try{
            this.salida=new FileOutputStream(direccion);
            this.salida.write(imagen);
            mensaje="Archivo Guardado";
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Archivo no guardado");
        }
        
        return mensaje;
    }
    
